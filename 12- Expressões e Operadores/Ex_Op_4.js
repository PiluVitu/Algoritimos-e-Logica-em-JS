// Operadores Aritméticos

let valueOne = 3

let valueTwo = 4

// Multiplicação
console.log(valueOne * valueTwo)
// Divisão
console.log(valueOne / valueTwo)
// Soma
console.log(valueOne + valueTwo)
// Subtração
console.log(valueOne - valueTwo)
// Resto da divisão
console.log(valueOne % valueTwo)
// Incremento
console.log(++valueOne)
// Decremento
console.log(--valueTwo)
// Exponencial
console.log(valueOne ** valueTwo)
