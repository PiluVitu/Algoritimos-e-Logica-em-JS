// Operator Condicional (Ternário)

// Dependendo da condição, nós recebemos valores diferentes

// Condição então valor 1 se não valor 2
// Condition ? value1 : value2

//Exemplos
//Café da manhã top
const pao = true
const queijo = true

let breakfast =
  pao && queijo ? 'Café da Manhã top pae' : 'Café foi uma merda...'

console.log(breakfast)

// Maior de 18
const age = 18

const over18 = age >= 18 ? '🍷🗿' : '🚨🚨🚨ALERTA🚨🚨🚨'

console.log(over18)
